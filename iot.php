			<h2>Microcontrollers</h2>
			<p><img src="img/microcontroller.jpeg" style="float: left;width:20%;margin:0 15px 15px 15px;">
			Microcontrollers are the brains of all smart devices. Acting on data received from sensors, microcontrollers apply pre-programmed logic to the sensor data received, and take different actions, based on different conditions.
			Mixed signal microprocesors can accept both digital and analog data from sensors, and process both accordingly. Microcontroller technology has gotten so affordable that any object can be embedded with a microprocessor 'brain'
			for only a few dollars. Their low power consumption makes them ideal for embedding into battery operated devices. We can program and incorporate any one of the wide range of microprocesors available today into your application,
			and use it not only to report sensor data, but also to receive commands and instructions from a web interface, or another device, based on certain conditions.</p>

			<div class="clearfix"></div>
			<h2>Protocols</h2>
			<p><img src="img/rs232.jpg" style="float: right;width:20%;margin:0 15px 15px 15px;">
			A device protocol is a 'language' of communication. Just as two people agree that they are going to have a conversation in 'English', over some other widely accepted language, devices too have a number of different
			protocols, or 'languages' that they can agree to speak to eachother in. There are many established communication protocols in existence today. Among them are RS-232, SPI, and I2C. The type of microprocessor used will
			determine which protocol is used. Any kind of data can be transmitted between microprocessors using one of the existing protocols. Or, if your application requires it, we can work with you to design and implement a new
			protocol specific to your application.</p>

			<div class="clearfix"></div>
			<h2>TCP/IP Converters</h2>
			<p><img src="img/wiznet.jpg" style="float: left;width:20%;margin:0 15px 15px 15px;">
			Converters take microcontroller data, supplied in one of the protocols mentioned above, package it into TCP/IP 'envelopes', and send it out over the internet. These devices have standard RJ-45 ethernet connectors, and connect
			directly to internet routers. This connection gives the data from devices access to the World Wide Web. Device data can then be sent anywhere. Also, internet connected devices can receive data too. This allows devices to be controlled
			from anywhere, using the World Wide Web. Imagine being able to control everything in your life from your smartphone or other mobile, internet connected device. The combination of technology required to make this a reality
			exists today. Let us make it work for you!
			</p>

