			<h2>Sensors</h2>
			<p><img src="img/sensors.jpg" style="float: right; width:20%;margin:0 15px 15px 15px;">
			One of the key components of any system are the sensors. If it moves, beats, changes temperature or some other state or characteristic, sensors are what will generate data related to that change, and send it to a listening
			microcontroller for processing. One of the more exciting advances in sensor technology are accelerometers. These low cost devices can accurately measure object orientation, acceleration and movement in any direction. There
			are literally thousands of low cost sensor solutions available on the market today, with more new ones being introduced every day. We have the ability to interpret sensor data, and process it in meaningful ways that are
			important to your application. We can work with sensors you have already selected, or we can help you find sensors that are just right for your needs.</p>

